    public void guardar()  throws SQLException 
    {
       
      MysqlDataSource datopersonas = new MysqlDataSource();
      datopersonas.setServerName("localhost");
      datopersonas.setDatabaseName("sistema_de_ventas");
      datopersonas.setUser("root");
      datopersonas.setPassword("");
      
      Connection conexion = datopersonas.getConnection();
      
      Statement sentencia = conexion.createStatement();
      
      try
      {
        String nombre = txtnombre.getText();
        String direccion = txtdireccion.getText();
        String correo= txtcorreo.getText();
        int telefono = Integer.valueOf(txttelefono.getText());
        int dni = Integer.valueOf(txtdni.getText());  
        
        PreparedStatement prep= conexion.prepareStatement("INSERT INTO clientes(nombre, direccion, correo, telefono,dni) VALUES (?,?,?,?,?)");
        prep.setString(1, nombre);
        prep.setString(2, direccion);
        prep.setString(3, correo);
        prep.setInt(4, telefono);
        prep.setInt(5, dni);
        
        prep.executeUpdate();
        JOptionPane.showMessageDialog(rootPane, "cliente guaradado");
        limpiartxt();
        
      } catch (Exception e)
      {
        JOptionPane.showMessageDialog(rootPane, "ingrese los datos correctos");
      }     
    }
    
   
